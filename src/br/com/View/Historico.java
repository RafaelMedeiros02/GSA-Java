    public void setar() {
        mH.setEstado("");
        mH.setNacionalidade("");
        mH.setNascimento("");
        rm = "";
        nome = "";
        hist.jFormattedTextFieldDataNasc.setText("");
        hist.jComboBoxEstados.setSelectedItem("Acre");
        hist.jTextFieldCidadeNasc.setText("");
        hist.jTextFieldNacionalidade.setText("");
        hist.jTextFieldNome.setText("");
        hist.jTextFieldRA.setText("");
        hist.jTextFieldRM.setText("");
        // =========================================
        int setar = jTablePesquisaHistorico.getSelectedRow();
        rm = (jTablePesquisaHistorico.getModel().getValueAt(setar, 0).toString());
        nome = (jTablePesquisaHistorico.getModel().getValueAt(setar, 1).toString());
    }

    public void pesquisaCampos() {
        mH.setRm(Integer.parseInt(rm));
        hdao.pesquisaCampos(mH);
        hist.jTextFieldNome.setText(mH.getNome());
        hist.jTextFieldRA.setText(mH.getRa());
        hist.jTextFieldRM.setText(String.valueOf(mH.getRm()));
        hist.jFormattedTextFieldDataNasc.setText(mH.getDataNasc());
    }
    
    public void pesquisaHistorico() {
        
        mH.setRm(Integer.parseInt(rm));
        hdao.pesquisaHistorico(mH);
        hist.jComboBoxEstados.setSelectedItem(mH.getEstado());
        hist.jTextFieldNacionalidade.setText(mH.getNacionalidade());
        hist.jTextFieldCidadeNasc.setText(mH.getNascimento());
    }
    
