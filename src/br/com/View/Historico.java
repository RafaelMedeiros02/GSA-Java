    public void pesquisaAlunos_porNome() {

        DefaultTableModel tabelaAlunos = (DefaultTableModel) jTablePesquisaHistorico.getModel();
        HistoricoDAO hD = new HistoricoDAO();
        try {
            tabelaAlunos.setNumRows(0);
            for (ModelHistorico mH : hD.listarAlunos(jTextFieldPesquisaNome.getText())) {
                tabelaAlunos.addRow(new Object[]{
                    mH.getRm(),
                    mH.getNome()
                });
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }

    }

